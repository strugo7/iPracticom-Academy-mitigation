export interface ServiceQualityProps extends Omit<
  SVGProps<SVGSVGElement>,
  'name'
> {
  /** Quality level: chooses needle position and gradient color. */
  level: ServiceQualityLevel
  /** Rendered height in pixels (width follows the aspect ratio). Defaults to 27. */
  size?: number
}

export function ServiceQuality({
  level,
  size = 27,
  className,
  ...rest
}: ServiceQualityProps) {
  const v = variants[level]
  const [, , w, h] = v.viewBox.split(' ').map(Number)
  return (
    <svg
      width={(w / h) * size}
      height={size}
      viewBox={v.viewBox}
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      role="img"
      aria-label={`Service quality ${level}`}
      {...rest}
    >
      {v.body}
    </svg>
  )
}
